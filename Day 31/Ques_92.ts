// This function removes the last element from an array and returns it
let favCars:string [] = ["Toyota Corolla", "Toyota CH-R", "Toyota Yaris", "Honda City"];
favCars.pop(); // Removes: 'Honda City'

console.log(favCars); // Outputs: ["Toyota Corolla", "Toyota CH-R", "Toyota Yaris"]
// Here, we take out the last car and show the updated list.
